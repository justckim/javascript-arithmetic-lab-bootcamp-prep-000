function add(a, b){
  return a + b
}

function subtract(a, b){
  return a - b
}

function multiply(a, b){
  return a * b
}

function divide(a, b){
  return a / b
}

function inc(n){
  n++
  return n
}

function dec(n){
  n--
  return n
}

function makeInt(a){
  var n = a
  parseInt(n, 10) 
  return n
} 

function preserveDecimal(a){
  var n = a
  parseFloat(n)
  return n
}